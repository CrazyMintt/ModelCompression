def _strip_for_json(r):
    """Remove arrays numpy que não são serializáveis em JSON."""
    return {k: v for k, v in r.items() if k not in ("preds", "labels")}

def _load_baseline_from_json(path):
    """
    Carrega resultados do baseline a partir de um JSON salvo anteriormente.
    Retorna um dict com as mesmas chaves que full_evaluation() produz,
    exceto 'preds' e 'labels' (que não são gravados no JSON).
    """
    print(f"\nLoading frozen baseline results from {path}...")
    with open(path, 'r') as f:
        data = json.load(f)

    # Suporta tanto {"target": {...}} quanto o dict direto
    if "target" in data and isinstance(data["target"], dict):
        results = data["target"]
    elif "baseline" in data and isinstance(data["baseline"], dict):
        results = data["baseline"]
    else:
        results = data

    print(f"  Baseline weights:  {results.get('weights', 'unknown')}")
    print(f"  Baseline accuracy: {results.get('top1', float('nan'))*100:.2f}%")
    return results

