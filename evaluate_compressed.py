def count_nonzero_params(model):
    total, nonzero = 0, 0
    quantized_types = (
        torch.nn.quantized.Conv2d, torch.nn.quantized.Linear,
        torch.nn.intrinsic.quantized.ConvReLU2d, torch.nn.intrinsic.quantized.LinearReLU,
    )

    for name, mod in model.named_modules():
        if isinstance(mod, quantized_types):
            try:
                w = mod.weight()
                data = w.int_repr().float()
                total += data.numel()
                nonzero += int((data != 0).sum().item())
                if hasattr(mod, 'bias') and mod.bias() is not None:
                    b = mod.bias().float()
                    total += b.numel()
                    nonzero += int((b != 0).sum().item())
            except: pass
        elif isinstance(mod, (nn.Conv2d, nn.Linear)):
            if hasattr(mod, 'weight'):
                data = mod.weight.data
                total += data.numel()
                nonzero += int((data != 0).sum().item())
            if hasattr(mod, 'bias') and mod.bias is not None:
                data = mod.bias.data
                total += data.numel()
                nonzero += int((data != 0).sum().item())

    return total, nonzero

def compute_macs(model, input_shape, device):
    macs = [0]
    def conv_hook(m, _inp, out):
        _, _, h, w = out.shape
        # Captura informações mesmo com tensores quantizados
        k_w, k_h = m.kernel_size if isinstance(m.kernel_size, tuple) else (m.kernel_size, m.kernel_size)
        kops = k_w * k_h
        macs[0] += (m.in_channels // m.groups) * kops * m.out_channels * h * w
        
    def linear_hook(m, _inp, _out):
        macs[0] += m.in_features * m.out_features

    handles = []
    for module in model.modules():
        if isinstance(module, nn.Conv2d) or 'Conv2d' in str(type(module)):
             handles.append(module.register_forward_hook(conv_hook))
        elif isinstance(module, nn.Linear) or 'Linear' in str(type(module)):
             handles.append(module.register_forward_hook(linear_hook))

    model.eval()
    with torch.no_grad():
        x = torch.zeros(1, *input_shape, device=device)
        model(x)

    for h in handles:
        h.remove()
    return macs[0]

def theoretical_size_mb(num_params, bytes_per_param):
    return num_params * bytes_per_param / 1024**2

def benchmark_latency(model, device, batch_size, n_runs=50, warmup=10):
    model.eval().to(device)
    x = torch.randn(batch_size, 3, IMG_SIZE, IMG_SIZE, device=device)
    times = []
    with torch.no_grad():
        for _ in range(warmup):
            y = model(x); float(y.flatten()[0])
        for _ in range(n_runs):
            t0 = time.perf_counter()
            y = model(x); float(y.flatten()[0])
            times.append((time.perf_counter() - t0) * 1000.0)
    times = np.array(times)
    return {
        "mean_ms":         float(times.mean()),
        "std_ms":          float(times.std()),
        "p50_ms":          float(np.percentile(times, 50)),
        "p95_ms":          float(np.percentile(times, 95)),
        "p99_ms":          float(np.percentile(times, 99)),
        "throughput_imgs": float(batch_size * 1000.0 / times.mean()),
    }

@torch.no_grad()
def evaluate_accuracy(model, loader, device):
    model.eval().to(device)
    all_preds, all_labels = [], []
    for imgs, lbls in loader:
        imgs = imgs.to(device)
        logits = model(imgs)
        preds = logits.argmax(1)
        all_preds.extend(preds.cpu().numpy())
        all_labels.extend(lbls.numpy())
    return np.array(all_preds), np.array(all_labels)

def full_evaluation(arch, weights_path, model, loader, device, classes, batch_sizes=[1, 32], n_runs=50):
    total, nonzero = count_nonzero_params(model)
    buffers = sum(b.numel() for b in model.buffers())
    macs = compute_macs(model, (3, IMG_SIZE, IMG_SIZE), device)

    latencies = {
        str(bs): benchmark_latency(model, device, bs, n_runs=n_runs)
        for bs in batch_sizes
    }

    preds, labels = evaluate_accuracy(model, loader, device)
    top1 = float((preds == labels).mean())
    f1_macro = float(f1_score(labels, preds, average="macro"))
    f1_weighted = float(f1_score(labels, preds, average="weighted"))
    per_class = classification_report(labels, preds, target_names=classes, digits=4)

    return {
        "arch": arch,
        "weights": weights_path,
        "params_total": total,
        "params_trainable": 0,
        "buffers": buffers,
        "macs": macs,
        "flops": macs * 2,
        "file_size_mb": os.path.getsize(weights_path) / 1024**2,
        "fp32_size_mb": theoretical_size_mb(total, 4),
        "fp16_size_mb": theoretical_size_mb(total, 2),
        "int8_size_mb": theoretical_size_mb(total, 1),
        "latencies": latencies,
        "top1": top1,
        "f1_macro": f1_macro,
        "f1_weighted": f1_weighted,
        "per_class_report": per_class,
        "preds": preds,
        "labels": labels,
        "nonzero_params": nonzero
    }

