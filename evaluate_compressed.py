class QuantizableBasicBlock(nn.Module):
    expansion = 1
    def __init__(self, inplanes, planes, stride=1, downsample=None,
                 groups=1, base_width=64, dilation=1, norm_layer=None):
        super().__init__()
        if norm_layer is None: norm_layer = nn.BatchNorm2d
        self.conv1 = nn.Conv2d(inplanes, planes, kernel_size=3, stride=stride,
                               padding=1, groups=groups, bias=False, dilation=dilation)
        self.bn1 = norm_layer(planes)
        self.relu = nn.ReLU(inplace=True)
        self.conv2 = nn.Conv2d(planes, planes, kernel_size=3, stride=1,
                               padding=1, groups=groups, bias=False, dilation=dilation)
        self.bn2 = norm_layer(planes)
        self.downsample = downsample
        self.stride = stride
        self.skip_add = torch.nn.quantized.FloatFunctional()

    def forward(self, x):
        identity = x
        out = self.relu(self.bn1(self.conv1(x)))
        out = self.bn2(self.conv2(out))
        if self.downsample is not None:
            identity = self.downsample(x)
        out = self.skip_add.add(out, identity)
        out = self.relu(out)
        return out

def build_model(num_classes=15):
    model = models.resnet18(weights=None)
    in_features = model.fc.in_features
    model.fc = nn.Sequential(
        nn.Dropout(p=0.3),
        nn.Linear(in_features, num_classes),
    )
    return model

def load_weights_into_quantizable(state_dict, num_classes=15):
    q_model = build_model(num_classes)
    try:
        q_model.load_state_dict(state_dict, strict=True)
    except:
        pass

    for layer_name in ['layer1', 'layer2', 'layer3', 'layer4']:
        layer = getattr(q_model, layer_name)
        for i in range(len(layer)):
            old = layer[i]
            nb = QuantizableBasicBlock(
                inplanes=old.conv1.in_channels,
                planes=old.conv1.out_channels,
                stride=old.stride,
                downsample=old.downsample,
            )
            nb.conv1.weight.data.copy_(old.conv1.weight.data)
            nb.bn1.weight.data.copy_(old.bn1.weight.data)
            nb.bn1.bias.data.copy_(old.bn1.bias.data)
            nb.bn1.running_mean.copy_(old.bn1.running_mean)
            nb.bn1.running_var.copy_(old.bn1.running_var)
            nb.conv2.weight.data.copy_(old.conv2.weight.data)
            nb.bn2.weight.data.copy_(old.bn2.weight.data)
            nb.bn2.bias.data.copy_(old.bn2.bias.data)
            nb.bn2.running_mean.copy_(old.bn2.running_mean)
            nb.bn2.running_var.copy_(old.bn2.running_var)
            layer[i] = nb
    return q_model

def fuse_resnet_modules(model):
    model.eval()
    torch.quantization.fuse_modules(model, ['conv1', 'bn1', 'relu'], inplace=True)
    for layer_name in ['layer1', 'layer2', 'layer3', 'layer4']:
        layer = getattr(model, layer_name)
        for block in layer:
            torch.quantization.fuse_modules(block, ['conv1', 'bn1', 'relu'], inplace=True)
            torch.quantization.fuse_modules(block, ['conv2', 'bn2'], inplace=True)
            if block.downsample is not None:
                torch.quantization.fuse_modules(block.downsample, ['0', '1'], inplace=True)
    return model

