eval_tf = transforms.Compose([
    transforms.Resize(256),
    transforms.CenterCrop(IMG_SIZE),
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225]),
])

class TransformSubset(torch.utils.data.Dataset):
    def __init__(self, dataset, indices, transform):
        self.dataset = dataset
        self.indices = indices
        self.transform = transform
    def __len__(self): return len(self.indices)
    def __getitem__(self, i):
        img, label = self.dataset[self.indices[i]]
        return self.transform(img), label

def get_test_loader():
    full_dataset = datasets.ImageFolder(DATA_DIR)
    all_classes = full_dataset.classes
    valid_class_idx = {all_classes.index(c): new_i for new_i, c in enumerate(CLASSES_15) if c in all_classes}
    filtered_samples = [(path, valid_class_idx[lbl]) for path, lbl in full_dataset.samples if lbl in valid_class_idx]
    full_dataset.samples = filtered_samples
    full_dataset.targets = [lbl for _, lbl in filtered_samples]

    labels = np.array(full_dataset.targets)
    sss1 = StratifiedShuffleSplit(n_splits=1, test_size=0.15, random_state=SEED)
    _, test_idx = next(sss1.split(np.zeros(len(labels)), labels))

    test_ds = TransformSubset(full_dataset, test_idx, eval_tf)
    return DataLoader(test_ds, batch_size=64, shuffle=False, num_workers=4)

