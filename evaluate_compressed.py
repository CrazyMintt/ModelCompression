def _strip_for_json(r):
    return {k: v for k, v in r.items() if k not in ("preds", "labels")}

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--weights", type=str, required=True)
    parser.add_argument("--quantized", action="store_true")
    parser.add_argument("--baseline", type=str, default=None)
    parser.add_argument("--device", type=str, default=None)
    parser.add_argument("--save-json", type=str, default=None)
    args = parser.parse_args()

    if args.device:
        device = torch.device(args.device)
    else:
        device = torch.device('cuda' if torch.cuda.is_available() and not args.quantized else 'cpu')

    print(f"Using device: {device}")

    loader = get_test_loader()

    # Load target model
    target_model = load_any_model(args.weights, is_quantized=args.quantized, device=device)

    print("\nEvaluating target model...")
    target_results = full_evaluation("resnet18", args.weights, target_model, loader, device, CLASSES_15)

    t_acc = target_results["top1"]
    t_size = target_results["file_size_mb"]
    t_latency = target_results["latencies"]["1"]["mean_ms"]
    t_total = target_results["params_total"]
    t_nz = target_results["nonzero_params"]

    print(f"\n{'='*40}")
    print(f" TARGET MODEL: {os.path.basename(args.weights)}")
    print(f"{'='*40}")
    print(f" Accuracy:          {t_acc*100:.2f}%")
    print(f" Size on disk:      {t_size:.2f} MB")
    print(f" Latency (batch=1): {t_latency:.2f} ms")
    print(f" Total Params:      {t_total:,}")
    print(f" Non-zero Params:   {t_nz:,}")
    print(f" Sparsity:          {100*(1-t_nz/t_total):.1f}%")

    baseline_results = None
    if args.baseline:
        print("\nEvaluating baseline model...")
        base_model = load_any_model(args.baseline, is_quantized=False, device=device)
        baseline_results = full_evaluation("resnet18", args.baseline, base_model, loader, device, CLASSES_15)

        b_acc = baseline_results["top1"]
        b_size = baseline_results["file_size_mb"]
        b_latency = baseline_results["latencies"]["1"]["mean_ms"]

        print(f"\n{'='*40}")
        print(f" COMPARISON (Target vs Baseline)")
        print(f"{'='*40}")
        print(f" Accuracy Delta:    {100*(t_acc - b_acc):+.2f} pp")
        print(f" Compression Ratio: {b_size/t_size:.2f}x")
        print(f" Speedup:           {b_latency/t_latency:.2f}x")

        agreement = (target_results["preds"] == baseline_results["preds"]).mean()
        print(f" Agreement:         {100*agreement:.2f}%")

    print("\nDetailed Classification Report (Target):")
    print(target_results["per_class_report"])

    if args.save_json:
        payload = {"target": _strip_for_json(target_results)}
        if baseline_results:
            payload["baseline"] = _strip_for_json(baseline_results)

        os.makedirs(os.path.dirname(args.save_json) or '.', exist_ok=True)
        with open(args.save_json, 'w') as f:
            json.dump(payload, f, indent=2)
        print(f"\nJSON report saved to: {args.save_json}")

